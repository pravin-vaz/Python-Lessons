'''This program will accept some values of the sides of a triangle to spit  out 
the area. 
'''
base = float(input("Enter the base of the trianglein metres: "))
height = float(input("Enter the height of the triangle in metres: "))
area = 0.5*base*height
print("The area of the triangle is: ", area, "sq.m")

